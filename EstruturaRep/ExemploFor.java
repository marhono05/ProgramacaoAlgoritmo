package EstruturaRep;

public class ExemploFor {

    public static void main(String[] args) {

        int i;
        // I de iterador


        // (Valor inicial da variável; Valor final da variável; Incremento)
        for(i=0; i<=9; i++){
            System.out.println("Etec de Fernandópolis");
        }
    }
    
}
